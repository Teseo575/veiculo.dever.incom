package MegustaelArte;

public class ObreaArte {

	private String nombre;
	private String autor;
	private String pais;
	public ObreaArte(String nombre, String autor, String pais) {
		this.nombre = nombre;
		this.autor = autor;
		this.pais = pais;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	
	
	
	
	
	
}
